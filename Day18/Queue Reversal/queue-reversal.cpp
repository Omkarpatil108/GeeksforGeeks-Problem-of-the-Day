//Function to reverse the queue.
class Solution
{
    public:
    queue<int> rev(queue<int> q)
    {
        // add code here.
        {
        vector<int> V;
        while(!q.empty())
        {
            V.push_back(q.front());
            q.pop();
        }
        while(V.size())
        {
            q.push(V[V.size()-1]);
            V.pop_back();
        }
        return q;
    }
    }
};


