class Solution{
    public:
    long long findMinDiff(vector<long long> a, long long n, long long m){
    //code
    sort(a.begin(),a.end(),greater<int>());
    vector<int>v;
    int i=0;
    int k=i+m-1;
    while(k<n){
        v.push_back(a[i]-a[k]);
        i++;
        k++;
    }
    sort(v.begin(),v.end());
    return v[0];
    
    }   
};

